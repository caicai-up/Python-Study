# 定义一个变量保存学生名字
StudentName = "张三"
# 定义一个变量保存年龄
StudentAge = 18

# 打印
print(StudentName)
print(StudentAge)

# 1. 定义一个整数变量
a = 100  # a变量就是一个整数类型的变量，int类型
print(type(a))

# 2. 定义一个浮点变量
b = 3.14  # b变量就是一个浮点类型的变量，float类型
print(type(b))

# 3. 定义一个布尔变量
c = True  # c变量就是一个布尔类型的变量，bool类型
print(type(c))

# 4. 定义一个字符串变量
d = "Hello World"
print(type(d))

# 5. 定义一个列表变量
e = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print(type(e))

# 6. 定义一个元组变量
f = (1, 2, 3, 4, 5, 6, 7, 8, 9)
print(type(f))

# 7. 定义一个字典变量
g = {"name": "张三", "age": 18}
print(type(g))

# 8. 定义一个集合变量
h = {1, 2, 3}
print(type(h))
